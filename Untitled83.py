        "        # Sous-couche 2 : Feed-Forward réseau avec Pre-LN et connexion résiduelle\n",
        "        norm_x = self.norm2(x)\n",
        "        ffn_out = self.ffn(norm_x)\n",
        "        x = x + ffn_out\n",
