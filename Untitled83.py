        "class SingleHeadAttention(nn.Module):\n",
        "    \"\"\"\n",
        "    Task 1: Implémentation de la Scaled Dot-Product Attention (Tête unique).\n",
        "    Calcule l'attention de base et extrait les poids pour inspection.\n",
        "    \"\"\"\n",
        "    def __init__(self, d_model):\n",
        "        super().__init__()\n",
        "        self.d_model = d_model\n",
        "        self.q_proj = nn.Linear(d_model, d_model)\n",
        "        self.k_proj = nn.Linear(d_model, d_model)\n",
        "        self.v_proj = nn.Linear(d_model, d_model)\n",
        "\n",
        "    def forward(self, x, mask=None):\n",
        "        B, T, C = x.shape\n",
        "        Q = self.q_proj(x)\n",
        "        K = self.k_proj(x)\n",
        "        V = self.v_proj(x)\n",
        "\n",
        "        # Formule de l'attention Scaled Dot-Product\n",
        "        scores = torch.matmul(Q, K.transpose(-2, -1)) / math.sqrt(C)\n",
        "\n",
        "        if mask is not None:\n",
        "            scores = scores.masked_fill(mask == 0, -1e9)\n",
        "\n",
        "        attention_weights = F.softmax(scores, dim=-1)\n",
        "        out = torch.matmul(attention_weights, V)\n",
        "        return out, attention_weights\n",
        "\n",
        "\n",
        "class MultiHeadAttention(nn.Module):\n",
        "    \"\"\"\n",
        "    Task 2: Extension de l'attention simple au mécanisme Multi-Head.\n",
        "    Divise l'espace en 'num_heads' branches de calcul parallèles avec Dropout.\n",
        "    \"\"\"\n",
        "    def __init__(self, d_model, num_heads, dropout=0.1):\n",
        "        super().__init__()\n",
        "        assert d_model % num_heads == 0, \"d_model doit être divisible par num_heads !\"\n",
        "        self.d_model = d_model\n",
        "        self.num_heads = num_heads\n",
        "        self.d_k = d_model // num_heads\n",
        "\n",
        "        self.qkv_proj = nn.Linear(d_model, 3 * d_model)\n",
        "        self.out_proj = nn.Linear(d_model, d_model)\n",
        "        self.dropout = nn.Dropout(dropout)\n",
        "\n",
        "    def forward(self, x, mask=None):\n",
        "        B, T, C = x.shape\n",
        "\n",
        "        # Projection linéaire combinée pour optimiser le calcul\n",
        "        qkv = self.qkv_proj(x)\n",
        "        qkv = qkv.reshape(B, T, 3, self.num_heads, self.d_k)\n",
        "        Q, K, V = qkv.unbind(dim=2)\n",
        "\n",
        "        # Transposer pour aligner le calcul parallèle : [B, num_heads, T, d_k]\n",
        "        Q = Q.transpose(1, 2)\n",
        "        K = K.transpose(1, 2)\n",
        "        V = V.transpose(1, 2)\n",
        "\n",
        "        # Calcul parallèle de l'attention scaled dot-product\n",
        "        scores = torch.matmul(Q, K.transpose(-2, -1)) / math.sqrt(self.d_k)\n",
        "        if mask is not None:\n",
        "            scores = scores.masked_fill(mask == 0, -1e9)\n",
        "\n",
        "        attn_weights = F.softmax(scores, dim=-1)\n",
        "        attn_weights_drop = self.dropout(attn_weights)\n",
        "\n",
        "        # Recombinaison spatiale et projection de sortie\n",
        "        attn_out = torch.matmul(attn_weights_drop, V)\n",
        "        attn_out = attn_out.transpose(1, 2).reshape(B, T, C)\n",
        "        output = self.out_proj(attn_out)\n",
        "        return output, attn_weights\n",
        "\n",
