        "class TransformerEncoderBlock(nn.Module):\n",
        "    \"\"\"Un bloc d'encodage complet combinant Multi-Head Attention et FFN.\"\"\"\n",
        "    def __init__(self, d_model, num_heads, d_ff, dropout=0.1):\n",
        "        super().__init__()\n",
        "        self.mha = MultiHeadAttention(d_model, num_heads, dropout)\n",
        "        self.ffn = PositionWiseFeedForward(d_model, d_ff, dropout)\n",
        "        self.norm1 = nn.LayerNorm(d_model)\n",
        "        self.norm2 = nn.LayerNorm(d_model)\n",
        "\n",
        "    def forward(self, x, mask=None):\n",
        "        # Sous-couche 1 : Attention avec Pre-LN et connexion résiduelle\n",
        "        norm_x = self.norm1(x)\n",
        "        attn_out, attn_weights = self.mha(norm_x, mask)\n",
        "        x = x + attn_out\n",
